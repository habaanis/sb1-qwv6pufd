export function useFormTranslation() {
  const { language } = useLanguage();

  const t = (category: 'labels' | 'placeholders' | 'buttons' | 'messages', key: string): string => {
    return formTranslations[language]?.[category]?.[key] || formTranslations.fr[category][key] || key;
  };

  return {
    t,
    language,
    submission_lang: language,
    label: (key: string) => t('labels', key),
    placeholder: (key: string) => t('placeholders', key),
    button: (key: string) => t('buttons', key),
    message: (key: string) => t('messages', key),
  };
}

