// Deep merge récursif pour fusionner les objets de traductions
const deepMerge = (target: any, source: any): any => {
  if (!source || typeof source !== 'object') return target;
  if (!target || typeof target !== 'object') return source;
  if (Array.isArray(source)) return source;

  const result: any = { ...target };

  for (const key of Object.keys(source)) {
    if (source[key] !== undefined) {
      if (typeof source[key] === 'object' && !Array.isArray(source[key]) && source[key] !== null) {
        result[key] = deepMerge(result[key] || {}, source[key]);
      } else {
        result[key] = source[key];
      }
    }
  }

  return result;
};

const deepMergeWithFallback = (target: any, fallback: any): any => {
  if (!target || typeof target !== 'object') {
    return target !== undefined ? target : fallback;
  }
  if (Array.isArray(target)) {
    return target;
  }
  const result: any = {};
  const allKeys = new Set([...Object.keys(target || {}), ...Object.keys(fallback || {})]);

  for (const key of allKeys) {
    if (target[key] === undefined) {
      result[key] = fallback[key];
    } else if (typeof target[key] === 'object' && !Array.isArray(target[key])) {
      result[key] = deepMergeWithFallback(target[key], fallback?.[key]);
    } else {
      result[key] = target[key];
    }
  }
  return result;
};

export const useTranslation = (lang: Language) => {
  // Deep merge de translations[lang] avec dictionary[lang]
  const baseTranslations = translations[lang] || {};
  const dictionaryTranslations = (typeof dictionary !== 'undefined' && dictionary?.[lang]) ? dictionary[lang] : {};
  const currentLangTranslations = deepMerge(baseTranslations, dictionaryTranslations);

  // Deep merge de translations.fr avec dictionary.fr pour le fallback
  const baseFallback = translations.fr;
  const dictionaryFallback = (typeof dictionary !== 'undefined' && dictionary?.fr) ? dictionary.fr : {};
  const fallbackTranslations = deepMerge(baseFallback, dictionaryFallback);

  if (lang === 'fr') {
    return currentLangTranslations;
  }

  return deepMergeWithFallback(currentLangTranslations, fallbackTranslations);
};

